
row, col = map(int, input().split())

temp_row = row
temp_col = col
dot_pattern = 1

# First half
for i in range(1, int((temp_row-1)/2)+1):
    for i in range(1, int((temp_col-3)/2)+1):
        print("-", end="")
    for i in range(1, dot_pattern+1):
        print(".|.", end="")
    for i in range(1, int((temp_col-3)/2)+1):
        print("-", end="")
    print()
    temp_col -= 6
    dot_pattern += 2

# Welcome
for i in range(1, int((col-7)/2)+1):
    print("-", end="")

print("WELCOME", end="")

for i in range(1, int((col-7)/2)+1):
    print("-", end="")
print()

# Second half
temp_row = row
dot_pattern = (int((temp_row-1)/2)*2)-1

for i in range(1, int((temp_row-1)/2)+1):
    for i in range(1, temp_col+1):
        print("-", end="")
    for i in range(1, dot_pattern+1):
        print(".|.", end="")
    for i in range(1, temp_col+1):
        print("-", end="")
    print()
    temp_col += 3
    dot_pattern -= 2


